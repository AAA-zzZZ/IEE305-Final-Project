BEGIN TRANSACTION;
CREATE TABLE "Design" (
	"ID"	INTEGER,
	"Code Name"	TEXT,
	"Chassis Type"	TEXT,
	"Unit Cost"	INTEGER,
	"Unit Revenue"	INTEGER,
	PRIMARY KEY("ID")
);

CREATE TABLE "Team" (
	"ID"	INTEGER,
	"Name"	TEXT,
	"Starting Cash"	INTEGER,
	"Vehicle Design ID"	INTEGER,
	PRIMARY KEY("ID"),
	UNIQUE("Name"),
	FOREIGN KEY("Vehicle Design ID") REFERENCES "Design"("ID")
);

CREATE TABLE "Vehicle" (
	"Chassis Serial Number"	TEXT,
	"Team ID"	INTEGER,
	"Purchase Time"	TEXT,
	"Sold Time"	TEXT,
	"Status"	TEXT,
	PRIMARY KEY("Chassis Serial Number"),
	FOREIGN KEY("Team ID") REFERENCES "Team"("ID")
);


